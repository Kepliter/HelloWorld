public class IfElseIfElse {
	public static void main (String[] args) {
		int a = 111;
		if (a==10) {
			System.out.println ("a = " + 10);
		} else if (a == 20) {
			System.out.println ("a = " + 20);
		} else if(a==30) {
			System.out.println ("a = " + 30);
		}else {
			System.out.println ("这是else语句");
		}
	}
}